package basico;

public class TipoInt {
    public static void main(String[] args) {
        // Declarando a variável do tipo int
        int idade = 22;
        System.out.println(idade);

        // Soma
        idade = 30 + 10;
        System.out.println(idade);

        // Precendencia
        idade = (7 * 5) + 2;
        System.out.println(idade);

        // Concatenação
        System.out.println("Idade: " + idade);
    }
}
